#Operador 'in' e 'not in'
# Verifica se um valor está dentro de uma 
# sequência (como listas, tuplas, strings, etc.)
frutas = ['maçã', 'banana', 'laranja', 'uva']

# Verifica se 'banana' está na lista de frutas
print('banana' in frutas)  # Saída: True

# Verifica se 'abacaxi' não está na lista de frutas
print('abacaxi' not in frutas)  # Saída: True

# Verifica se a letra 'a' está na string 'banana'
print('a' in 'banana')  # Saída: True

# Verifica se a letra 'z' não está na string 'banana'
print('z' not in 'banana')  # Saída: True

#Podemos colocar também uma string "inteira",
# como por exemplo: nana se eu perguntar se in ou not in,
# ele consegue verificar se tem nana no texto que pedi 

print(10 * '-')

